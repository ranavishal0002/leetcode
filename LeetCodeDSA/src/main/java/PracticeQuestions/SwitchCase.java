package PracticeQuestions;


public class SwitchCase {
    public static void main(String[] args) {
        int a =5;
        int b=6;
         switch (a+b){

             case 11:{
                 System.out.println("good answer");
                 break;
             }
             case 12:{
                 System.out.println("not good answer");
                 break;
             }
             default:{
                 System.out.println("no answer");
                 break;
             }

         }
    }
}
